class Demo1_Const {  //常量
	public static void main(String[] args) {

		/*
		- 字符串常量(用双引号括起来的内容)
		- 整数常量(所有整数)
		- 小数常量(所有小数)
		- 字符常量(用单引号括起来的内容,只能是单个数字、字母或者符号)
		- 布尔常量(true和false)
		- 空常量(null)
		*/

		System.out.println("双引号字符串");  //字符串常量
		System.out.println(12312);			//整数
		System.out.println(213.123);		//小数常量
		System.out.println(' ');			// 字符常量,可以为空格
		//System.out.println('');  //错误的
		//System.out.println('10'); //错误的
		System.out.println(false);			//布尔常量
	}
}
